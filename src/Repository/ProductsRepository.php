    public function findAllByCategorie(){
        return $this->createQueryBuilder('p')
            ->select('NEW App\\DTO\\productsDTO(p.id,
                p.title,
                c.categories,
                p.priceWithDiscount,
                p.price,
                p.description,
                p.discount,
                p.imageName)')
            ->innerJoin('p.categories',"c")
            ->orderBy('p.id', 'ASC')
            ->getQuery()
            ->getResult();
    }
